const {app,BrowserWindow} = require('electron')
createwindow = ()=>
{
    const newapp = new BrowserWindow({
        width: 800,
        height: 800,
        resizable: false,
        fullscreenable: false,
        webPreferences: {nodeIntegration: true},
        icon: 'icon.ico'
    })
    newapp.loadFile('index.html')
}
app.whenReady().then(createwindow)

